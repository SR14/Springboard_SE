// for loop 0 to 10
for (let i = 0; i <= 10; i++) {
    console.log(i);
};

// while loop 0 to 10
let i = 0;
while (i <= 10) {
    console.log(i);
    i++
};

// for loop 0 to 20 (even)
for (let i = 0; i <= 20; i += 2) {
    console.log(i)
};

// while look 0 to 20 (odd)
i = 1;
while (i < 20) {
    console.log(i);
    i += 2
};

//  for loop 10 to 0
for (let i = 10; i >= 0; i--) {
    console.log(i)
};